FORBIDDEN_KEYWORDS = {
    # Наркотики и вещества
    "нарк", "марихуана", "гашиш", "кокаин", "героин", "амфетамин",
    "мдма", "lsd", "опиат", "спайс", "синтетика", "наркотик",
    # Мошенничество и обман
    "развод", "кидалово", "обман", "мошенни", "обналич", "отмыв",
    "преступ", "воров", "краж", "грабёж", "угон", "взлом",
    # Незаконная деятельность
    "проститут", "секс", "порн", "интим", "интимные услуги",
    "казино", "ставки", "гемблинг", "игровой зал", "лотерея",
    # Оружие и насилие
    "оружие", "пистолет", "ружьё", "боеприпас", "взрывчат", "насили",
    # Другое
    "поддел", "фальшив", "взятк", "коррупц", "рэкет"
}

def contains_forbidden_content(text: str) -> bool:
    """Проверяет, содержит ли текст запрещённые слова."""
    if not text:
        return False
    text_lower = text.lower()
    for word in FORBIDDEN_KEYWORDS:
        if word in text_lower:
            return True
    return False